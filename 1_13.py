lengd = int(input("Sláðu inn lengd rétthyrningsins í cm:")) #biður um lengd í cm
breidd = int(input("Sláðu inn breidd rétthyrningsins í cm:")) #biður um breidd í cm
flatarmal = lengd * breidd #reiknar flatarmal Lengd*breidd
print(F"Flatarmál rétthyrningsins er: {flatarmal}cm2") #gefur upp flatarmál í cm